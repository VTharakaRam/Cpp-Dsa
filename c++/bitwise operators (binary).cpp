#include <iostream>
using namespace std;

int main()
{
	cout<<(7&13)<<endl;
	cout<<(5|10)<<endl;
	cout<<endl;
	
	//***********SHIFTING OPERATORS*********
	cout<<"LEFT SHIFT"<<endl;
	cout<<(5<<2)<<endl;
	cout<<(15<<3)<<endl;
	cout<<(3<<4)<<endl;
	cout<<endl;
	
	cout<<"RIGHT SHIFT"<<endl;
	cout<<(15>>2)<<endl;
	cout<<(30>>4)<<endl;
	cout<<(80>>3)<<endl;
	
	
	
	cout<<"<<<<<<<<<<<<<<<<<<<"<<endl;
	cout<<(~8)<<endl;
	
	
	return 0;
}
